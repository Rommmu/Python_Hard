# 숫자를 바로 대입하기 
print("I am Skhu LikeLion %dth. \n" % 10)

# 문자를 바로 대입하기 
print("I am Skhu %s 10th. \n" % "LikeLion")

# 같은 문장이 출력되시나요 ?? 

# 숫자 변수를 이용해 대입하기
date = 10
print("Today is the %dth. \n" % date)

# 문자 변수를 이용해 대입하기
day = "Tuesday"
print("Today is %s. \n" % day)

# 2개 이상의 값 대입하기
month = 5
week = 2
study = "Python 심화1"
print("%d월 %d번째 주 %s 강의 입니다. \n" % (month, week, study))

# %s로 변수 형태 상관없이 출력하기
present = 10
sentence = "할 만 해요 !"
print("오늘은 %s일 입니다. \n Python 심화 %s" % (present, sentence))