# 숫자를 바로 대입하기 -- 본인의 학번을 대입해주세요
print("제 학번은 %d입니다." % 202114041)


'''
예시 : 
제 학번은 202114041입니다.
'''


# 문자 변수를 이용해 대입하기 -- 학과 or 학부를 대입해주세요
school = "IT융합자율학부"
print("저는 %s입니다." % school)


'''
예시 :
저는 IT융합자율학부입니다.
'''


# 소수 변수를 이용해 대입하기 -- 원주율은 3.141592로 사용합니다
